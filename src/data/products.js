export function getAllProducts() {
  const all = [];
  for (const [catSlug, cat] of Object.entries(products)) {
    for (const sub of cat.subcategories) {
      for (const product of sub.products) {
        all.push({ ...product, category: catSlug, subcategory: sub.name });
      }
    }
  }
  return all;
}

export function getProductsByCategory(categorySlug) {
  return products[categorySlug] || null;
}

export function getProduct(categorySlug, productSlug) {
  const cat = products[categorySlug];
  if (!cat) return null;
  for (const sub of cat.subcategories) {
    for (const product of sub.products) {
      if (product.slug === productSlug) {
        return { ...product, category: categorySlug, subcategory: sub.name, categoryLabel: cat.label };
      }
    }
  }
  return null;
}
